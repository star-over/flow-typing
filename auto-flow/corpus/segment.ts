/**
 * @file Шаг 2 конвейера: нарезка очищенного текста на единицы-упражнения.
 *
 * Единица drill — одно законченное предложение (либо одно слово). Источник
 * OpenCorpora уже сегментирован: один `<source>` = одно предложение, поэтому
 * сейчас единица = вся непустая строка. Полноценная разбивка абзаца на
 * предложения (с обходом сокращений «т. д.», инициалов) появится, когда придёт
 * несегментированный источник — отдельным усложнением этого шага.
 */

export function segment(text: string): string[] {
  const trimmed = text.trim();
  return trimmed.length > 0 ? [trimmed] : [];
}
