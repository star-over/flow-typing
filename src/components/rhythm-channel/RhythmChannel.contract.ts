/**
 * Theme contract for RhythmChannel.svelte.
 *
 * Контракт-тест в `src/themes/contract.test.ts` проверяет, что каждая тема
 * декларирует токены из этого списка.
 *
 * RhythmChannel — «рецептор ритма» под курсором FlowLine: тонкая полоса с мягким
 * поясом-допуском по центру, неподвижным рецептором «сейчас» и маркером-кромкой,
 * которая сбегает к рецептору под гравитацией. Цвета маркера — семантические
 * токены исходов, а не палитра-украшение: в зоне = держишь темп, вне зоны =
 * снесло (частишь/тормозишь). Сторону сноса несёт позиция маркера и сторона
 * заливки-состояния, а не оттенок — обе стороны одним цветом. Обратная связь
 * «вне зоны» на ступень мягче ошибки набора, но обязана быть видна — конкретный
 * цвет выбирает читаемость на фоне темы (см. DESIGN «Правило мягкости ритма»;
 * поэтому в sepia — мягкий красный, где янтарь на пергаменте тонет). Правило
 * «тихого хрома» держится: насыщенность отдана исходам ритма.
 *
 * Форма значения у каждого токена помечена в комментарии: цвет (для `background`)
 * либо полное значение `border`.
 */
export const RHYTHM_CHANNEL_CONTRACT = [
  '--rhythm-channel-track-background', // фон полосы (цвет)
  '--rhythm-channel-track-border',     // рамка всей полосы (1px solid …)
  '--rhythm-channel-zone-fill',        // заливка пояса-допуска, края растворяет компонент (цвет, мягкий)
  '--rhythm-channel-marker-in',        // маркер/рецептор в зоне — держишь темп (цвет); им же светится рецептор
  '--rhythm-channel-marker-outside',   // маркер вне зоны — частишь ИЛИ тормозишь (цвет; sepia — мягкий красный, прочие темы — янтарь)
  '--rhythm-channel-state-fill',       // заливка-состояние полосы при тапе вне зоны (цвет, мягкий)
  '--rhythm-channel-anchor',           // рецептор «сейчас» в покое — нейтральная вертикаль (цвет)
  '--rhythm-channel-trail',            // дорожка-след от рецептора к кромке (цвет, приглушённый)
] as const satisfies readonly `--${string}`[];

export type RhythmChannelContractToken = (typeof RHYTHM_CHANNEL_CONTRACT)[number];
